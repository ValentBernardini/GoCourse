package main
import "fmt"

func main(){
	a:= 2 

	//a = a+2
//suma en asignacion
	a += 2
	fmt.Println(a)
	//resta en asignacion
	a -= 2
	fmt.Println(a)
	//multiplicacion en asignacion
	a *= 2
	fmt.Println(a)
	//division en asignacion
	a /= 2
	fmt.Println(a)
	//modulo en asignacion
	a %= 2
	fmt.Println(a)
	//operador de incremento
	a++
	//operador de decremento
	a--
}